/**
 *Hacer un pseudocodigo que imprima los numeros del 100 al 0, en orden
decreciente
 * @author j3llyic3
 */
public class Ejercicio02 {
    public static void main(String[] args) {
        for(int i=100;i<=0;i++)
        {
           System.out.println(i);
        }
    }
    
}
