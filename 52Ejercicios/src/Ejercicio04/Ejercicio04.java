
package Ejercicio04;

/**
 *Hacer un programa que imprima la suma de los 100 primeros numeros.
 * @author j3llyic3
 */
public class Ejercicio04 {
    public static void main(String[] args) {
        int total=0;
        for(int i=0;i==100;i++)
        {
          total=total+i;
        }
        System.out.println("La suma es: "+total);
    }
}
